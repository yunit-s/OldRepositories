package test4_circle;

public class Point {

	// constructors
	public Point() {
	}
	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}

	// fields
	private int x, y;
	
	// methods

	// getter & setter
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

}
